package Oct.Ex6_Constants_Literals;

public class Lab007 {
    public static void main(String[] args) {

        // Constants

        // Integer constant
        final int MAX_SCORE = 100;

        // Double constant
        final double PI = 3.14159;

        // Boolean constant
        final boolean IS_ENABLED = true;

        // Character constant
        final char GRADE_A = 'A';

        // String constant
        final String APP_NAME = "MyApplication";

        System.out.println("Max Score: " + MAX_SCORE);
        System.out.println("Value of PI: " + PI);
        System.out.println("Is Enabled: " + IS_ENABLED);
        System.out.println("Grade A: " + GRADE_A);
        System.out.println("Application Name: " + APP_NAME);
    }
}
