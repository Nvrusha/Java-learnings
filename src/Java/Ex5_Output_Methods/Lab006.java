package Java.Ex5_Output_Methods;

public class Lab006 {
    public static void main(String[] args) {
        // Output methods

        // 1. printf()
        int age = 25;
        System.out.printf("My age is %d. %n", age); // %d is a placeholder for integers

        int num = 10;

        System.out.printf("%d X %d = %d", num, 1, num);

        // Summary
//      print(): Outputs without newline.
//      println(): Outputs with newline.
//      printf(): Outputs formatted strings.
//      format(): Formats strings, returns String.
//      write(): Writes characters/bytes directly.


    }
}
